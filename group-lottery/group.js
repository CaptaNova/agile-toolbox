function shuffle(arr) {
  for (let i = arr.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * i);
    const temp = arr[i];
    arr[i] = arr[j];
    arr[j] = temp;
  }
}

function isXmas() {
  const date = new Date();
  return date.getMonth() === 11 && date.getDate() <= 25;
}

